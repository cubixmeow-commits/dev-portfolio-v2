    <h2>Who SousMeow Is For</h2>
    <p>SousMeow exists for people who struggle to get consistently good results from AI.</p>
    <p>It is built for people who struggle to get reliable results from ChatGPT, Claude, Gemini, and other AI assistants — people who do not want to become prompt engineers, workflow designers, or domain experts just to finish a project.</p>
    <p>Cookbooks must transfer expertise without requiring expertise.</p>
    <p>If using a Cookbook ever feels like learning a new profession, the Cookbook has failed this law.</p>

    <h2>The Law</h2>

    <h3>1. Cognitive load is the product</h3>
    <ul>
      <li>The product is not the workflow.</li>
      <li>The product is the cognitive load the workflow removes.</li>
      <li>A Cookbook succeeds when the user reaches a finished result without carrying the professional judgment that produced it.</li>
    </ul>

    <h3>2. Expertise transfer, not expertise demand</h3>
    <ul>
      <li>Cookbooks should transfer expertise without requiring expertise.</li>
      <li>The Cookbooks contain the expertise.</li>
      <li>Users should not need to learn prompt engineering, learn AI workflows, understand implementation details, or become experts.</li>
    </ul>

    <h3>3. Hide judgment; ask only for known facts</h3>
    <ul>
      <li>Workflow complexity may be high <strong>internally</strong>.</li>
      <li>User-facing complexity must remain <strong>low</strong>.</li>
      <li>The workflow should hide professional judgment whenever possible.</li>
      <li>The workflow should ask only for facts the user actually knows.</li>
      <li>The workflow must never invent those facts.</li>
      <li>Pantry ingredients are facts the user owns. Prompts, sequencing, checks, and domain judgment stay inside the Cookbook.</li>
    </ul>

    <h3>4. Stay below the Complexity Threshold</h3>
    <p>The <strong>Constraint</strong> under this law is: <strong>Stay Below the Complexity Threshold.</strong></p>
    <p>Internal process may be rich. External experience must stay simple enough that a struggling AI user can still finish.</p>
    <p>If a proposal requires the user to perform expert work they do not already do, it is above the threshold.</p>

    <h3>5. Complexity Scorecard</h3>
    <p>Before authoring, score the proposal against the <strong>Complexity Scorecard</strong>. Checks include (at minimum):</p>
    <ol>
      <li><strong>Audience fit</strong> — usable by someone who struggles with AI, not only by experts?</li>
      <li><strong>Expertise demand</strong> — does the user need new professional skill, or only facts they already know?</li>
      <li><strong>Judgment visibility</strong> — is professional judgment hidden inside the workflow, or pushed onto the user?</li>
      <li><strong>Fact honesty</strong> — does every required input ask for something the user can actually know? Does the workflow refuse to invent missing facts?</li>
      <li><strong>Surface simplicity</strong> — would a first-time user describe this as guided steps, or as learning a new craft?</li>
      <li><strong>Failure test</strong> — if using this Cookbook feels like learning a new profession, the design fails this check.</li>
    </ol>

    <h3>6. Complexity Gate</h3>
    <p>Every Cookbook proposal <strong>must</strong> pass the <strong>Complexity Gate</strong> <strong>before authoring</strong>.</p>
    <ul>
      <li>Run the Complexity Scorecard on the proposal.</li>
      <li>Cookbooks failing <strong>two or more</strong> scorecard checks must be <strong>simplified or rejected</strong>.</li>
      <li>A failed gate is not a soft suggestion. Authoring does not begin until the proposal passes.</li>
      <li>Marketplace Cookbooks are bound by this law. Internal reference workflows that fail the gate must not ship as marketplace Cookbooks.</li>
    </ul>

    <h2>Case: “Make, Not Generated” (expert version)</h2>
    <p>The current expert version of <strong>“Make, Not Generated”</strong> fails this law.</p>
    <p>It demands too much expert judgment from the user, sits above the Complexity Threshold, and fails multiple Complexity Scorecard checks.</p>
    <ul>
      <li>It must not be authored or published as a marketplace Cookbook.</li>
      <li>The expert workflow should remain an <strong>internal reference only</strong>, not a marketplace Cookbook.</li>
      <li>Any future public version must be redesigned to pass the Complexity Gate first.</li>
    </ul>

